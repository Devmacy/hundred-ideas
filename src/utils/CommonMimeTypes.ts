/**
 * 根据文件扩展名获取MIME类型。
 * @param {string} extension - 文件扩展名。
 * @returns {string} 对应的MIME类型，如果找不到则为空字符串。
 */
function getMimetype(extension: string): string {
    return extensionsMap.get(extension.toLowerCase()) || "";
}

/**
 * 根据MIME类型获取文件扩展名。
 * @param {string} mimetype - MIME类型。
 * @returns {string} 对应的文件扩展名，如果找不到则为空字符串。
 */
function getExtensions(mimetype: string): string[] {
    return mimetypesMap.get(mimetype.toLowerCase()) || [];
}

